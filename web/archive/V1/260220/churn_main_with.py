# 🟪 가설5 함수
# "총통화시간 대비 상담전화건수 비율이 높으면 해지 가능성 증가"
# ▪ 변수 : 상담강도(파생) = 상담전화건수 / 가입일
# ▪ Y축  : 해지율(%)
# ▪ 그래프 : 막대그래프
# ============================================================
def get_df5():

    # 1️⃣ DB 조회
    with engine.connect() as conn:
        df_5 = pd.read_sql("""
            SELECT
                CASE
                    WHEN (상담전화건수 / (가입일 + 0.001)) <= 0.01
                        THEN '1.낮음(~0.01)'
                    WHEN (상담전화건수 / (가입일 + 0.001)) <= 0.03
                        THEN '2.중간(0.01~0.03)'
                    WHEN (상담전화건수 / (가입일 + 0.001)) <= 0.06
                        THEN '3.높음(0.03~0.06)'
                    ELSE
                        '4.매우높음(0.06+)'
                END AS 상담강도구간,
                COUNT(*) AS 전체건수,
                COUNT(CASE WHEN 전화해지여부 = 1 THEN 1 END) AS 해지건수,
                ROUND(
                    COUNT(CASE WHEN 전화해지여부 = 1 THEN 1 END) * 100.0 / COUNT(*),
                2) AS 해지율
            FROM TRAIN
            GROUP BY
                CASE
                    WHEN (상담전화건수 / (가입일 + 0.001)) <= 0.01
                        THEN '1.낮음(~0.01)'
                    WHEN (상담전화건수 / (가입일 + 0.001)) <= 0.03
                        THEN '2.중간(0.01~0.03)'
                    WHEN (상담전화건수 / (가입일 + 0.001)) <= 0.06
                        THEN '3.높음(0.03~0.06)'
                    ELSE
                        '4.매우높음(0.06+)'
                END
            ORDER BY 상담강도구간
        """, conn)

    # 2️⃣ 차트 데이터 가공
    df_5["상담강도구간_표시"] = df_5["상담강도구간"].str[2:]

    chart_data_5 = {
        "labels": df_5["상담강도구간_표시"].tolist(),  # X축: 구간명
        "values": df_5["해지율"].tolist(),              # Y축: 해지율(%)
        "counts": df_5["해지건수"].tolist(),
        "totals": df_5["전체건수"].tolist(),
        "y_label": "해지율 (%)",
        "title": "가설5: 상담강도(상담전화/가입일) 구간별 해지율"
    }

    # 3️⃣ 반환
    return chart_data_5


