# 🟦 가설1 함수
# "상담전화건수가 많을수록 해지 건수가 높다" (불만 고객 가설)
# ▪ Y축 : 해지건수 (율 아님!)
# ▪ X축 : 상담전화건수 (0,1,2,3...11)
# ▪ 그래프 : 막대그래프
# ============================================================
def get_df1():

    # 1️⃣ DB 조회
    with engine.connect() as conn:
        df_1 = pd.read_sql("""
            SELECT
                상담전화건수,
                COUNT(CASE WHEN 전화해지여부 = 1 THEN 1 END) AS 해지건수,
                COUNT(*) AS 전체건수
            FROM TRAIN
            GROUP BY 상담전화건수
            ORDER BY 상담전화건수
        """, conn)

    # 2️⃣ 차트 데이터 가공
    chart_data_1 = {
        "labels": df_1["상담전화건수"].tolist(),   # X축: 0,1,2,3...11
        "values": df_1["해지건수"].tolist(),        # Y축: 해지건수
        "totals": df_1["전체건수"].tolist(),        # 참고용
        "y_label": "해지 건수",
        "title": "가설1: 상담전화건수별 해지 건수"
    }

    # 3️⃣ 반환
    return chart_data_1


# ============================================================
# 🟩 가설2 함수
# "가입일이 짧은 고객일수록 해지 확률이 높다" (초기 이탈 가설)
# ▪ Y축 : 해지율(%)
# ▪ X축 : 가입일 구간 (신규/중간/오래/장기)
# ▪ 그래프 : 라인차트
# ============================================================
def get_df2():

    # 1️⃣ DB 조회
    with engine.connect() as conn:
        df_2 = pd.read_sql("""
            SELECT
                CASE
                    WHEN 가입일 <= 100  THEN '1.신규(~100일)'
                    WHEN 가입일 <= 300  THEN '2.중간(101~300일)'
                    WHEN 가입일 <= 500  THEN '3.오래(301~500일)'
                    ELSE                     '4.장기(500일+)'
                END AS 가입구간,
                COUNT(*) AS 전체건수,
                COUNT(CASE WHEN 전화해지여부 = 1 THEN 1 END) AS 해지건수,
                ROUND(
                    COUNT(CASE WHEN 전화해지여부 = 1 THEN 1 END) * 100.0 / COUNT(*),
                2) AS 해지율
            FROM TRAIN
            GROUP BY
                CASE
                    WHEN 가입일 <= 100  THEN '1.신규(~100일)'
                    WHEN 가입일 <= 300  THEN '2.중간(101~300일)'
                    WHEN 가입일 <= 500  THEN '3.오래(301~500일)'
                    ELSE                     '4.장기(500일+)'
                END
            ORDER BY 가입구간
        """, conn)

    # 2️⃣ 차트 데이터 가공
    # 앞 번호 제거: "1.신규(~100일)" → "신규(~100일)"
    df_2["가입구간_표시"] = df_2["가입구간"].str[2:]

    chart_data_2 = {
        "labels": df_2["가입구간_표시"].tolist(),  # X축: 신규, 중간, 오래, 장기
        "values": df_2["해지율"].tolist(),          # Y축: 해지율(%)
        "counts": df_2["해지건수"].tolist(),        # 참고용
        "totals": df_2["전체건수"].tolist(),        # 참고용
        "y_label": "해지율 (%)",
        "title": "가설2: 가입일 구간별 해지율"
    }

    # 3️⃣ 반환
    return chart_data_2


